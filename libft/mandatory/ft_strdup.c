#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*copy;
	int		i;

	if (s == NULL)
		return (NULL);
	copy = malloc(sizeof(char) * ft_strlen(s) + 1);
	if (copy == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		copy[i] = s[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
